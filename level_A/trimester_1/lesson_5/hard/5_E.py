y = int(input())
x = int(input())

# создаем начальные состояния
dp = [([0]*100)]*100
dp[0][1] = 1
dp[1][0] = 1
dp[1][1] = 2

# начинаем с первого неопределенного состояния
for i in range(1, x):
    for j in range(1, y):
        dp[i][j] += dp[i-1][j] + dp[i][j-1]



# печатаем результат
print(dp[x - 1][y - 1])
